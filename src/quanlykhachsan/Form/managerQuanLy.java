    private void butTraPhongActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_butTraPhongActionPerformed
        DateFormat simpleDateFormat = new SimpleDateFormat("yyyy-MM-dd");
        StringBuilder sb = new StringBuilder();
        Date date1 = null;
        Date date2 = null;
        String NgayTra = txtTraPhong.getText();
        String NgayVao = txtDateNow.getText();
        int SoPhong = 0;
        try {
            date1 = simpleDateFormat.parse(NgayTra);

        } catch (ParseException ex) {

            sb.append("\"Không đúng định dạng yyyy-MM-dd \"");
        }

        try {
            SoPhong = Integer.parseInt(txtSoPhong.getText());
        } catch (Exception ex) {
            sb.append("Để Không đúng định dạng , phải là Số\n");
        }
        if (sb.length() > 0) {
            JOptionPane.showMessageDialog(this, sb.toString(), "Thông Báo", HEIGHT);
        } else {
            for (Phong phong : listsv) {
                if (phong.getSoPhong() == SoPhong) {
                    phong.setNgayTra(date1);
                    quanly.TraPhong(phong);
                }
            }
            loadTable();
        }


    }//GEN-LAST:event_butTraPhongActionPerformed

