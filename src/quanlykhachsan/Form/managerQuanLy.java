    public void loadTable() {
        String[] b = new String[]{"MaPhong ", "So Phong", ",Gia Phong ", "TenKh", "CMND", "NgayVao", "NgayTra", "TongTien", "TinhTrang"};
        DefaultTableModel a = new DefaultTableModel(b, 0);

        List<Phong> listsv = new ArrayList<>();
        PhongDAo quan = new PhongDAo();
        String TinhTrang = null;
        a.setNumRows(0);
        long tongtien = 0;
        listsv = quan.getlist();
        for (Phong c : listsv) {
            if (c.isTinhTrang()) {
                TinhTrang = "Đang Sử Dụng";
            }
            
            if (c.TinhTongTien() != 0) {
                tongtien = c.TinhTongTien();
                c.setTongTien((long)tongtien);
            } else {
                tongtien = 0;
            }
            
            a.addRow(new Object[]{c.getMaPhong(), c.getSoPhong(), c.getGiaPhong(),
                c.getTenKh(), c.getCMND(), c.getNgayVao(), c.getNgayTra(),tongtien, TinhTrang});
        }
        TablePhong.setModel(a);
    }

